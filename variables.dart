void main() {
  // Declare a variable 'name' and assign a string value
  var name = "John";
  // Declare a variable 'age' and assign an integer value
  var age = 30;
  // Print name and age using string interpolation
  print("Name: $name, Age: $age");

  // Declare a String variable 'fullname'
  String fullname = "Harsh Jaiswal";
  // Print the full name
  print(fullname);

  // Declare a dynamic variable 'role' (can hold any type)
  dynamic role;
  // Assign a string value to 'role'
  role = "Student";
  // Print the role
  print(role);

  // Declare a constant 'height' (compile-time constant)
  const height = 180;
  // Declare a final variable 'weight' (run-time constant)
  final weight = 90;
  // Print weight and height
  print(weight);
  print(height);
}
